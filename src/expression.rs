use std::convert::From;

#[derive(Clone, Eq, PartialEq, Ord, PartialOrd, Hash, Debug)]
pub enum Expression<'a> {
    Nullary(NullaryOp<'a>),
    Unary(UnaryOp, Box<Expression<'a>>),
    Binary(BinaryOp, Box<(Expression<'a>, Expression<'a>)>),
}

impl<'a> From<NullaryOp<'a>> for Expression<'a> {
    fn from(op: NullaryOp<'a>) -> Self {
        Expression::Nullary(op)
    }
}

